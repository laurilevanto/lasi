<h2>Om glasmaterialet</h2>

<h3>Struktur</h3>

Glas är ett mycket speciellt material. Det är en fast lösning. 
Det har egenskaperna av vätskan 
(med undantag av att fluiditeten)
 som uppkommer bara när glaset uppvärms.)
  Molekylbindningar är inte regelbunda som i kristaller,
   men vissa är kraftigare de andra svagare.,
   Därför, har glaset <i>ingen  exakt smältpunkt</i>, 
   det blir progressivt mjukare. i värme över ca. 500 grader C.

<h3>Egenskaper</h3>

 Några av glasets grundläggande egenskaper är viktiga <br>
<ul>
<li> <i>hårdhet och styvhet</i> . 
Glas är mycket motståndskraftig mot slitage 
men inte till stöt eller böjning. </li>
-<li>Ytspänningen är det son håller sopbubblan i form. 
Om man sticker en prick i bubblan  bryts det upp i droppar. 
När man i glasskärning bryter ytspänningen från ena sidan 
har det likaså en tendens att spricka i tu. 
Man behöver bara böja litet och 
skärlinjen öppnas genom skivan.</li>

-<li><i>smältning.</i> När man värmar upp glaset tillräckligt, 
blir det gradvis flytande.
Det har ingen fast smältpunkt, Det beter sig mera som vax, 
som är hård i kylan och mjukare i värme.
 När man ökar värme, ser glaset först våt ut, 
 fast det ännu är solid. 
 Sen blir det morkt röd glödande, och vidare til gult och vitglöd.
Rödglöende glass kan böjas långsamt, 
gulglöenende räcker till att sammanfoga (fusa) glasbitar. 
Vitglöende glas rinner som syrup. Då kan man även blosa glas.
I olika faser av ugnsarbete söker man 
bäst kombination av värme och tid.</li>
</ul>
</p>

Motsatsen till värmning är kylning. Där kräver glas viss omsorg.
 Alla materialer expanderar med värme och minskar tillbaka 
 vid kylning.

 I kylsteget, molekylerna förlorar rörlighet och 
 glaset blir en fast fas vid 530-450 C°. 
 Om nån del av ett  glasföremål svalnar långsammare 
 fortsätter  det att minska fast omkringliggande delar är solida.
 Då förekommer en spänning inom glasstycket, 
 som gör det mycket svagare. .
  Då är glaset  under inbyggd stress som kan bryta föremålet 
   vid minsta provokation 
  - även efter flera år.

<h3>Kompatibilitetet</h3>

I förra paragraf handlades stress som var orsakad 
av ojämn avkylning.
Likadan stress förekommer om man försöker smälta ihop glas  
som har olika värme-expandering. 
Man måste välja glas som passar ihop.
 När fabriker producerar vanligt fönsterglas, tänker de inte alls 
 att att man skulle vilja smälta det ihop med nånting annat. 
 Samma gäller till vackra konstglas som är gjort för tiffasnyarbete. 
Lyckligtvis finns det ochså några firmor som har specialiserat sig
att tillverka färgade glas för sammansmältning.
