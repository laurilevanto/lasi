 <h2>Vad händer i glasugn?</h2>

Man kan smälta gjas i en speciell glasugn, 
men också i en traditionell keramikugn. 
När man börjar värma glas i ugnen, är det först ömtåligt 
till värmeshock. Har du inte bevittnat värmeshock 
när man häller alltför varmt vatten i en glas, 
eller sätter ett ömtåligten glasföremål i diskmaskinen. 
Samma risk har vi med smältugnen. 
Man måste värma upp långsamt.
<p/>

När uppvämning fortsätter sker nogonting som kallas fas skift. 
Hårt glas blir elastsker.
 Det händer nära 500 C. Glas har ingen fast smältpunk.
 Liksom vax blir det mjukare i högre värme, 
 och slutligen rinnande.
 <p/>

Där emellan finns ett temperaturområde där glaset är mjukt. 
Om man sätter en glasskiva på formen,
sjunker den långsamt in av sin egen vikt
 tills den följer formen.Det kallas <b>slumpning</b>.
  Ordet kommer från engelska. 
  Jag vet inte rätta svenska utrycket.
<p/>
I högre temperatur 740-820 C kan man sammansmälta  glasbitar. 
Det kallas <b>fusing</b>.
Det engelska ordet betyder sammansmältning.
<p/>
Vid en ännu högre temperatur (900 C) 
kan  man fylla formen med krossat glas, 
som smälter till en solid 3D föremål.
Litet högre till, ca 1500C är glas rinnande som sirap. 
Då kan man blåsa glas.
<p/>

Kylning har en särskild roll i behandlingen av glaset.
 Vid kylning stelnar glaset, och om detta sker ojämnt, 
 blir det stress i föremålet som sen "fryser in" pernmanent.
 Föremålet kan komma ut ur ugnen helt, 
 men kan spricka när som hällst av den minsta orsak.
 Glasblåsarna brukar lämna sina verk över natten 
 i en speciell ugn
 som avkylnar mycket långsamt.
