<h2>Slumpning</h2>
är att böja glas i ugn med hjälp av värme och tyngkraft.
I slumpning använder man formar. 
Formar kan man köpa i affärer som säljer fusingglas. 
Man kan även själv göra formar av lera eller keramiska fiberplattor. 
Engånsformar kan man också göra av gipsblandning.
Fater av rostfri stål kan man också använda.
<p>
Slumping sker oftast i en lägre temperatur än fusing.
Det  lägsta jag har användt är 650 C men oftast 720-740C.

<p>
När slupning sker med tyngkraft, 
en tung tjock föremål slumpas lättare en en lätt.
Också vida formar slumpar snabbareän smala. 
När kanterna är längre bort från varandra bär de inte så mycket.



<h3>Att slumpa in i en form</h3>
Där sätter man en platta ovanpå formen så den ligger i stöd av sina kanter.
Mitten sjunker sen ner tills den följer formen helt.

<p>
Det är svårt att gissa vad är det rätta temperatur och tid vid slumpning.
Det är bättre om man kan kika in i ugnen och följa förloppet.
 I kapitlet säkerheten lärde vi att man ska inte titta längre än 10-15 sekunder åt gången.
När slumpning har nått det önskade effect, följer kylning.


<h3>Att slumpa över formen</h3>
är en motsvarande teknik, men där blir formen  inom glaset.
Det varma glaset är mjukt och faller i väck som en bordsduk.

