<h2>Avkylning</h2>

<i> Detta kapitel handlar om tunna avkylning av tunna föremål 
som är fusade och/eller slumpade.
Om glaset är mer än 10 mm tjock måste man referera till 
fabrikens recommendationer.
.</i>
<h3>Initial avkylning</h3>
<p>
När glasföremålet är fuad, slumpad elle gjuten färdigt måste det avkylas
från ugnens hätta till romstemperatur.
När kviksilvern i termometer värmer reser den högre. När vädret blir kallare
minskar det i volymen och sjunker. Det samma händer med glas.
</p>
<p>
Strax efter arbetstemperaturen är glas forfarande glödande hett
och rinnande. I början vill man avkyla så snabbt som möjligt att 
stycken inte hinner ändra formen vidare.

<p>
Vid slumpning
 måste man avkyla särskilt snabbt att formen inte ändras.
 Då kan man oppna ugnen för en stund för att få värmen sjunka 50-100 grader.
 Slumppauksessa joutuu joskus aluksi jäähdyttämään rajusti ettei 
lasi valu pidemmälle kuin on tarkoitus.


<h3>Annealing</h3>
Rubriken är på engelska, jag vet inte det rätta svenska ordet, 
kanske avspänning.
Det betyder att man avkylar så långsamt att hela glasmasan 
är hela tiden inom 5 grader. 
Man börjar det med att hålla glaset en längre tid på en viss temperatur. 
För Bullseye det är 500C. För 3-6 mm glass räcker en halv timme.
Då är hela massan styv men ändo så flexibet att 
när en del svalnar och krympar orkar det dra resten med sig.
Då blir det ingen stress som kan "frysa in "iglaset och söndra et senare.

Saken blir liten mera kompicerad med att den tunga ungsplattan 
moste hinna med.

Med tjock glass kan det ta timmar och även dagar. 
Klassisk exempel var den 5 meter stora teleskopspegeln 
som behövde två år att avkyla.
</p>
<p>
När temperaturen har jamnats, 
kan man fortsätta avkylning med det adra steget,
men dock så långsamt att inget nytt stress 
om temperaturskillnaden kan formas.
</p>
<p>
Med tunna 3-9 mm föremål ugnens värmekapasitet är 
tillräckligt att hålla takten i gränser. 
För tjockare saker måste man programmera 
en långsammare avkylning


<h3>Slut avkylning</h3>
Ny har glaset blivit solid utan stress. Ny behöver man bara akta
att det inte förekommer termal shock som bryter föremålet.
Vanligen låtem man det hända övr natten.
<p>
Man får inte öppna ugn innan glas är så avkyld att 
man kan hantera det bekvämt med bara händer. 
</p>

<h3>Den kalla fasen</h3>
När man äntligen har fått föremålen ut ur ugnenär det bäst att genast tvätta 
separator daam bort. Vatten borde vara litet varmare än glaset. Ättika och
avkalkningsämne kan hjalpa till i tvätt. 
Om en omedelbar tvätt inte får separatorfläckar helt bot
låt den ligga över natten i ättika.
Ofta de sista små smulorna sitter fast i djupningar.
Tandläkarens sond eller en nål är bra värktyg.
<p>
Om du har fusat någonting som är mer än en centimeter tjock är det säkrast 
att låta den vila över natten innan man tvättar.
 
