  <h3>Flammpolering</h3>
  
  Ordet flammpolering har vi ärvt av glasblåsning.   Dom andvänder en gasfackla.  
  I en elugn har vi inte flammor men effekten blir densamma.
  Ytspänningen drar ojämheten bort och glaset får en klar blank yta.
 <p>
 Undersidan av glaset har varit mot ugnplattan 
 eller fiberpapper och blivit matt. Den kommer att bli insidan av
 vår fat. Insidan vill vi ha glänsande fin.
 
 </p>
