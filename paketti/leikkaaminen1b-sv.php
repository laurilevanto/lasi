<h2>Att skära glas 2</h2>

 <p>
<h3> Att skära med linjalen </h3>
Linjalen har en tendens att glida på glaset. 
En bit av mjukt material under linjalen hjälper. 
Jag har användt vanligt rullplåster.
Kom ihåg när du mäter glaset att skäraren har en tjoklek 
om 5 mm.
Spåret kommer till hälften av det, 2,5 mm från linjalen. 
Det är en smaksak on du skär bårtåt ellet inåt med linjalen
Ofta är det enklast att börja med den närmaste kanten.
Se upp att spåret börjar just från kanten. 
Vid slutkanten drar man över kanten.
</p>
<p>
Besök en glasaffär och be om några bortkastade bitar 
för att öva med.
Fönstergas är lättast att skära. 
Sen kan du pröva vad har du lärt med dyrare konstglas.
</p>
Man kan inte skära en rättvinkel bit från glasskivan. 
Spåret måste gå från kant till kant oavbrutet.
<p>
<h3>Att skära med fri hand</h3>
Böjda linjer måste man skära på fri hand. 
Dock kan man rita med tusch
var linjen ska gå. 
Med klart glas kan man skära på teckningen som syns
genom glaset.</p>
<p>
När man planerar böjda linjer måste man komma ihåg 
att slutligen öppnas spåret med att böja glas.
Öppningen har en tendess att gå vilse till utkanten.
 Även böjda linjer måste skäras från kant till kant.
 Sen kan man fortsätta med annu lite närmare 
 det slutliga mönstret.
</p>

<p>
Branta insida kurvor är nästan omöjligt att skära. 
Oftast måste man göra det stegvist
med arr börja med mindre kurvor.
<p>

