<h2>Fusing </h2>
betyder sammansmältning av glasbitar. Det kräver<br>
- hög temperatur där glasets natur som vätska kommer fram <br>
- tid att glasbitar hinner smälta ihop.

<p>Fusing kan ske i vidt temperaturåmrode. 
Den exrema är <i>djupfusing</i>
där den övre glasbiten sjunker hel flat med det nedre glaset.
Den andra exremen är <i>ytfusing</i>, 
när den övre biten håller sin raka kanter
men ändå "Klistras" till nedre glas. 
Där emenllan är konturfusing. där den övre biten håller sin form men
 dock har mjuka rundade kanter och fast den reser over basglaset.
</p><p>
I bilden är bladerna nästan djupfusade men 
blomman har skarpa kontur som ytfusad.
I sjäva värket var den gjord i en bränning på samma gång. 
Bladerna var Bullseye som är mera flytande 
och blomman var Artsta som är hårdare 
och behöve högre temperatur eller länge tid.
Kanter av blomman har rundats så litet att de inte skadar fingrar män ännu är skarpa.

 
<h3>Djupfusing</h3>
Det gröna Bullseye bladerna blev djupfusade med Bullseue basglas.
De är inte fullständigt djupfusade, de har litet kontur som ger em en lrnsform
som concentrerar ljuset vacket.
 <br>
I djupfusing värman glaset så att det blir ganska rinnande. Då ytspänning riundar kanterna
och den övre biten sjunker ned i basglas tills hela överfladen är flat och klart.
Om man djupfusar på ett klart glas en blå cirkel, blir resultatet en klar skiva med mjukkantad blå
prick. 
<p>
I djupfusingens temperatur kopierar glaset ugnplattans ytstruktur.
Resultatet är en matt baksida.

<h3>Ytfusing</h3>
Ytfusing sker i ett lägre temperatur och eller en kortare tid. Formen ändras inte så mycket,
bara kanterna rundades och ytan blir slåtare.
Om man ytfusar i mycket låg värme håller glaset styvheten mera och orkar inte kopiera underlaget så mycket.

