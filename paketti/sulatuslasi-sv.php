<h2> Glas för fusing</h2>
 De flesta konstglas är inte menade till sammansmältning. 
 De är för tiffany- och blyteknik där man arbetar med kallt glas. 
 Tillverkaren har sökt efter fina färger och nyancer 
 utan att tänka hur glaset beter sig i hettan. 
 <p/>
 
Fönsterglaset är rimligt i prisen och därför lockande. 
Det medför dock så många problem 
att jag vill inte recommendera, åtminstone inte för en nybörjare. 
Man måste känna dess begränsningar. 

 Återanvändnigsglas är tyvärr ännu mera krävande. 
 <p/>
 Åtminstone nedan listade fabrikerna gör specielt fusingglas. 
 De tar hand om att deras producter passar ihop  
 med deras egna varianter.  Man säger att de är kompatibla. 
 
Kompatibilititet betyder att olika färger 
har utvidgandet och viskositet passat ihop så 
att med rätt kylning förekommer ingen stress mellan glasbitar. 
Bullseye är den äldsta fabrik som specialiserade till fusing glas. 
I sista katalog hade de över 600 varianter. 
Allt Bullseye F-märk glas hat testats för kompabilitetet. 
Dom säljer också för tiffany bruk litet billigare glas , .
som inte har passerat deras test. 
Nordiska importörer brukar begränsa sortimentet till fusible glas. 

 Bullseye glass skivor är gjorda med att manuellt kavla. 
 Därför har den ganska ojämn yta som kan vara svår att skära.
  I klart glas har BE också en billigare Tekta serie, 
  som är maskinellt gjord. 
  <p/>
Gaffer är en fabrik i Nya Zeeland som tillverkar blykristall glas 
både klar och färgad. 
Blykristallen avvikar ljustrålar kraftigt. 
Det ger en speciell glans til glaset. Det är lätt att slipa. 
Tyvärr är det också tungt och dyrt. 
<p/>
Spectrum äeren stor fabrik som har utveklat en glas serie 
kallad System96. 
Idéen bakom var att det skulle vara kompatibel med andra konstglas,
 särskillt med nordiska märken. 
 Det lyckades inte så bra, 
 i praktiken bara S96 ar kompatibellt inom serien. 
 <p/>
 Spectrum har mer slät yta och det är en smula tunnare än Bullseye. 
 Det är betydligt lättare att skära. 
Bullseye och Spectrum  är inte kompatibla med varann.  
Därför de flesta artister väljer en eller annan för för att undvika fel. 
<p/>
Deco-lasi är ytfärgad fönsterglas. 
Det borde vara kompatibelmed vanlig fönsterglas, 
men när inte allt fönsterglas är kompatibelt med varandra 
kan man råka i problem. 
 Ofta lyckas det med Deco och fönsterglas. 
 När färg en i Deco är ett tunnt skikt på ytan 
 kan man lätt sandblåsa eller etcha figurer i färgen. 
 .Deco glas har en specialitet att om färytan är mot luft 
 håller det jämt men om färgytan är mot annat glas 
 formar det dekorativa små bubblor. 
 <p/>
 Deco glass säljs även som pulver, 
 som man kan använda i glassmålningar på fönsterglas. 
 Då blir färlagerett så tunn att det kan inte oraka okompabilität.. 
 <p/>
 
Schott Artista är en europeisk märke och 
därför litet billigare än de amerikanska. 
Sortimentet är inte särskilt stort. 
Glaset är maskinellt valsad och därför mycket slät och lätt att skära. 
Artista behöver litet högre temperatur än de andra. 
<p/>
 Schott har ett glas kallad Opalika.  Den är ytfärgad vit. 
 Det är gjord av samma glasmassa som Artista. 
 Opalika är kompatibelt med Artista, men bara med en nypa salt.
  Opalika kan ändras okompatibelt i den andra bränning. 
  <p/>
Deras productionsteknik hindrar att görä selenhaltiga glas. 
Schott har löst problemet med att beställa 
vissa gula och röda färger från Uroboros, 
som kan tillvärka manuellt olika varianter. 
Serien som är tillpassad med Artista och 
sälgs bara under Artista märket. 
Observera att den har en olik skivstorlek och en annorlunda yta. 
 I vissa fall kan Artista kombineras med Bullseye. 
<em> Men bara på egen risk! </em>
 Bägge firmaer påstor att de inte är kompatibla. 
 <p/>
Uroboros gör två serier av fusingglas. 
Uroboros COE 90 är riktad att vara kompatibelt med Bullseye, men inte testat att vara det. Uroboros COE 96 är compatibelt med Spectrum. Om du köper Uroboros, kolla att du får av den rätta serien. 
 Det finns flera märker som marknadsför vackra glas för Tiffany- arbetet. De flesta är inte kompatibla även inom samma fabrikat, desto mindre med de övriga märken eller med fusingglas.. Dock kan man sammasmälta Tiffanyglas om alla bitar är av samma skiva.
      
    
