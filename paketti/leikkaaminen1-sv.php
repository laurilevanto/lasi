Glas är hårt amorfisk material som har stor ytspänning.
När man bryter ytspänningen från ena sidan 
med att skära en linje med glasskäraren,
bryts glaset lätt enligt linjen, när det böjs bort från linjen
<p>
För att välja en glasskärare har jag bara ett råd. 
Skäraren är bra om den känns bekväm att använda.
Dock skall man välja sin skärare i en högre prisklass. 
Avgörande del är trissan som då är av bättre kvalitet.
De billigaste skärare har ett revolverhuvud med många trissor. 
Det berättar att även fabriken tror 
att den första trissan håller inte långt.
En bra trissa tål att dra flera kilometer linje.
</p>
<p>
Jag har märkt att olika människor har sina favorit märken.
Greppet på skäraren varierar.  
Det finns skärare med rakt eller böjt skaft.
Om man håller ett rakt skaft med samma grepp som en penna 
kan man lätt styra linjen.
När ett böjt skaft ligger mot handen är det lättare 
att få rätt tryck.
Det har ochså betydelse om man föredrar att skära mot sig 
eller bortåt.
Med ett böjt skaft är det lättast at skära bortåt, 
då ser man var skäraren går.
Med ett rakt skaft kan man skärä också inåt.
</p>
<p>
Det bästa underlaget är jämn och litet mjuk. 
En tunn gummimatta är bra.
En gammal handduk eller  ett par lager av gamla tidningar 
duger bra.
Tidningspappret har den fördel att glasskärvor 
går till avfall med pappret.
</p>
<p>
Akta för att det inte finns gamla skärvor 
eller annat ojämnhet under glasskivan.
Då kan skärarens tryck bryta glaset i en oönskad riktning. 
Bordet kan gärna vara ganska lågt.

</p>
<p>
Idén är att få trycket med kroppen, inte med handmuskeln.
Stöd övre armen mot kroppen ocl sätt bena efter varann.
Rörelsen sker med att flytta kroppvikt med ben, 
mindre med arm.
Vid behov kan du stöda skäraren sidlängs med den andra hand.
</p>
<p>
Man ska skära med ett långt drag från ena kanten til den andra. 
Det behöver dock inte vara snabbt.
För snabbt  drag kan få skäraren att hoppa 
om glasytan är ojämn.
Då får du inte ett kontinuerligt spår.
</p>
Du har rätt trycknär du hör ett svagt ljud. 
Ett bra spår är knappt synlig, 
man knappast känner det när man drar fingernageln tvärs. 
Om spåret har synligt ojämhet har du tryck för hårt.

 <p>
 Man måste öva intill att spåren lyckas hela vägen 
 med ett försök.
 Man ska inte försöka dra  ett nytt spår på det gamla. 
 Det ödelägger glasskäraren snabbt
 och gör det ännu svårare att bryta spåret opp.

 
