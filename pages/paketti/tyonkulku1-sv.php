	 <h2>Arbetsexempel</h2>

 
 
 <h3>Först skär man elementen</h3>
 
 Det vanligast är att man bygger ett föremål på ett helt underglas,
 eller eventuellt under ett helt ovanglas.
 Det gör man därför att om man bara lägger glasbitar 
 brevid varann,  kanterna drar en bit  och 
 det blir ett mellanrum som
  hindrar dem att bli sammansmälta.
I vissa fall kan man använda enkelt glas med 
att överlappa kanterna.
Det har jag gjort med mina små änglar. 
<p>  
Skärlinjerna passar inte alltid precis ihop. 
Då slipar man kanterna tills de passar.
När bitarna är på sin plats är det ofta bra att 
limma dem med nagellack eller modelflygplanlim. 
Lim håller arbetet i ordning <i>under vägen tilll ugnen</i>.
I ugn brinner lim bort mycket tidigare än 
glassbitarna blir sammanfusade.
Man måste planera bitarna att så att <i>de sitter på plats 
även utan lim</i>.
</p>
 <p>
 Som exempel har jag valt en kryssfat. 
 Det är inte det bästa som första arbetet men 
 ger en god översikt över olika faser.
 Den <a href="../lahi/pik-enkeli.php"> Lilla Ängeln</a>
 är en alternativ för din första experiment.
 
 <h3>Att skära glas</h3>
 Man skall inte börja med att skära en rämsa åt gången 
 från glasskivans kant.
 Den smala strimlan är fragil och bryter lätt 
 när man försöker öppna skråmen.
Glas är lättast att skära i mittet. 
 Då vet brytlinjen inte vilket håll den vill avvika.
 </p>
 <p> Bästa sättet är en kompromiss. 
 Man skär t.ex åtta linjer på en gång och först öppnar det innersta.
 Då har man en 8 linjer bred skiva. 
 Sen öppnar man den mellersta linjen.
 och därefter två och två och sist varje par i tu. 
  Smala strimlor öppnar man bäst med tång.
 </p>
 <p>
Om du gör 15 mm breda rämsor behöver du 20 st för en 30x30 cm fat. 
 </p>
  
