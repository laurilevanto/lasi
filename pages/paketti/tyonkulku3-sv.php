<h3>Sammansmältning</h3>
    
    Om du skall steka i en 
      <a href="../uuni/keram-uuni.php">keramikugn, </a>
    läs först mera råd  om dess egenskaper.
   <p>
   Nu har vi ett ämne som har 3 mm, 6mm och 9mm 
   tjocka områden och mycket luftutrymme där under. 
   Nu i ugnen skall det stekas så 
   att tjoklekens variationer nästan försvinner. 
   Meningen är att vi skall få en homogen glasplatta som 
   är slät på undersidan.
   
   </p>
Foglinjer som formas när det översta glaset sjunker ned 
är nästan fösvunnen. Det är mycker nära en full fusing.
och kräver en hög temperatur och 
tid att glaset hinner sjunka ned.
En översikt om stekning har vi haft tidigare.
    </p>
    <p>
    Tyvärr är ugnen olika så jag kan inte garantera 
    att programmet som jag beskriver är det bästa i ditt fall.
    </p>
    <p> 
    Man börjar med att sakta höja temperaturen till 520 C,
    då är man över risken av värmeschock
    Glaset är nu mera elastisk och man kan värma opp 
    så snabbt som ugnen hinner till 780 C. 
    Då är glaset rinnande och sjunker ned av sin egen vikt.
    Först blir  "broarna" av övre strimlor böjda nedåt, 
    sen sjunker de rättvinkelt mot ugnsplattan. 
   Samtidigt sprider de kryssande punkter vidare och 
   man kan inte längre se i vilken rktning var den övre. 
   Det tar ca 20 minuter. 
   Om man kan efter 10 minuter titta kan man då justera  tiden 
   kortare eller längre efter behov.
  </p>
