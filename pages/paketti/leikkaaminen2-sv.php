<h3>Att öppna skåran </h3>
<p> 
Efter skärning måste man öppna skåran.
<p>
1. Det säkraste sätt är att vända glaset med skåran  nedåt
 på ett litet flexibel underlag, och sen trycka med tummen 
 så att skåran öppnas genon hela skivans tjoklek.
 Det kan man göra endast med transparent glas 
 där man kan se genom var skåran är. 
 Även där är en fördel om man har ritat linjen med tusch.
 </p>
 <p>
2. den andra metoden är att man böjer glaset bortifrån 
skåran. 
Då spricker det efter linjen.
 </p>
 <p>
3. En lång rak linje är lättast att öppna med att föra skåran till bordkanten.
Med ena handen håller man glaset på plats mot bordet 
och med den andra handen tar man ett grepp på kanten 
så att biten kan inte falla på golvet. 
Litet tryck med den överhängande del oppnar skåran.
Låter skrammande men är mycket lätt.
Kom dock ihåg att använda skyddhanskar.
</p>
<p>
4.  En mycket lång linje mitt på glaset är lättast att öppna med 
att man sätter en penna   vid glaskanten under skåran.
Sen sätter man handflaten på bägge sidor av 
skåran och trycker ned.
Hela längden av skåran öppnas samtidigt.
</p>
<p>
5. Mindre glasbitar kan man bryta i händer. Tummen kommer 
vid skårans sidor och pekfingrar stöder underfrån.
</p>
 .
<p>
6. När man vill skära smala strimlor av glaskanten är de svåra att bryta av.
Då andvänder man bryttång.
</p>
<p>
7. En variant av tekniken 1. är att man tar en smal skruvmejsel och 
med plaster eller isoleringsband mjuknar huvudet. Man sätter mejseln
på baksidan, jämnt mot skåran och slår med handflaten på mejseln huvud.
</p>
8. I ytterst nödfall kan man försöka med att lätt knacka till skråmas baksida
med glasskärarens metalhuvud. 
Den metoden är vanlig i tiffanyarbetet men ger alrig en nätt kant.
Brytlinjen blir snedt med farligt skarpa kanter.
I tiffanyarbeten slipar man kanten ändå.
<p>
tt annat nöd trick är att göra en ny skråma  på andra sidan av glasskivan,
Oftast är det säkrast att ge opp och göra ett nytt försök  annastans.
