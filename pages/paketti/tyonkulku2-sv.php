 <h3>Sen bygger man plattan</h3>
 När man har skuritt strimlorna färdigt är det lättast att bygga på 
 ett vitt papper. Om du måste transportera föremålet 
 annanstans för stekning  köp <i>fiberpapper</i> och 
 limma även de nedersta strimlorna fast.
 
  I fiberpappret är cellulosafiberna ersatta med keramisk fiber 
  som inte brinner bort,   och inte heller fastnar till glas.
.
man kan ju bygga på ett underglas också.
    <p>
    Först ställer man glasstrimlorna i en  rikting till 
    med jämna avstånd, och sen den andra riktningen tvärs över.
    Kolla att  det blir ratt vinkel. 
  Det hela övre eller under glaset ska mätas så att 
  strimlornas änder blir ca 2 mm över kanten.
  Den ger en vacker pepparskaks kant när allt sammansmälts.
   
   .
    <p>
    När strimlorna är preliminärt på sin plats, är det tid at limma ihop.
    Först limmar du de yttersta hörnen. 
    Lim skall inte bli mellan glas men vid den övre strimlans kant.
    Låt hörnen torka innan du fortsätter. Sen limmar du
    från varje hörna den nästa strimlan
      </p>
      <p>
      Till sist sätter man <i>överglaset på </i>och limmar det ochså fast i några punkter.
